window.getLang = () => localStorage.getItem("lang") || "en";
window.setLang = (l) => { localStorage.setItem("lang", l); location.reload(); };
window.t = (key) => {
  const lang = window.getLang();
  return (window.I18N[lang] && window.I18N[lang][key]) || (window.I18N.en[key] || key);
};

window.applyTranslations = () => {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = window.t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    el.innerHTML = window.t(el.dataset.i18nHtml);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    el.placeholder = window.t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    el.title = window.t(el.dataset.i18nTitle);
  });
  document.documentElement.lang = window.getLang();
};
